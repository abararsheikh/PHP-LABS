//YOU HAVE 3 TABLES-1.CATEGORY 2.MPRODUCTS 3.PRODUCTS
//WE HAVE CREATED CLASS FOR THE CATEGORY TABLE
class Category {
    private $id;
    private $name;

    public function __construct($id, $name) {
        $this->id = $id;
        $this->name = $name;
    }

    public function getID() {
        return $this->id;
    }

    public function setID($value) {
        $this->id = $value;
    }

    public function getName() {
        return $this->name;
    }

    public function setName($value) {
        $this->name = $value;
    }
}
?>